vector<int> a[maxn];
bool check[maxn];
void dfs(int u){
  check[u]=true;
  stack<int> st;
  st.push(u);
  while(!st.empty()){
    int p=st.top();
    st.pop();
    for(auto& v:a[p]){
     if(check[v])
       continue;
     check[v]=1;
     st.push(v);
    }
  }
}
